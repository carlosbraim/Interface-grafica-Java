    private void btnArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnArquivoActionPerformed
        // TODO add your handling code here:
        
        //abre os aquivos do computador, para selecionar arquivos e diretorio
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Procurar arquivo");
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);//somente arquivos
        
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Imagem", "jpg","png");
        
        fileChooser.setFileFilter(filter);
        //fileChooser.showOpenDialog(this);
      
        //saber se o usuario selecionou um arquivo ou cancelou 
        int retorno = fileChooser.showOpenDialog(this);
        
        if(retorno == JFileChooser.APPROVE_OPTION){
            
            File file = fileChooser.getSelectedFile();//arquivo selecionado e seu caminho
            txtFile.setText(file.getPath());
            //apresentar imagem
            lbImagem.setIcon(new ImageIcon(file.getPath()));//setar imagem no label 
        }
    }//GEN-LAST:event_btnArquivoActionPerformed

